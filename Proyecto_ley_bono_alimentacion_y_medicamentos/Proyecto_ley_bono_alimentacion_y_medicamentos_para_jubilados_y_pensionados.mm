    <p>
      En virtud de la responsabilidad del Estado de garantizar la seguridad social y atenci&#243;n integral a las personas mayores, consagrada en el art&#237;culo 80 de la Constituci&#243;n Nacional que establece: &#8220;El Estado garantizar&#225; a los ancianos y ancianas el pleno ejercicio de sus derechos y garant&#237;as. El Estado, con la participaci&#243;n solidaria de las familias y la sociedad, est&#225; obligado a respetar su dignidad humana, su autonom&#237;a y les garantizar&#225; atenci&#243;n integral y los beneficios de la seguridad social que eleven y aseguren su calidad de vida&#8221;....
    </p>
    <p>
      
    </p>
    <p>
      Es por esto, que el Estado venezolano en aras de cumplir con las obligaciones establecidas en la constituci&#243;n y en calidad de m&#225;ximo garante de los derechos humanos y de los derechos de los adultos mayores. Debe garantizar una adecuada alimentaci&#243;n y el acceso a las medicinas mediante programas sociales que est&#233;n contenidos en el ordenamiento jur&#237;dico venezolano.
    </p>
    <p>
      
    </p>
    <p>
      Ese es el prop&#243;sito principal de este proyecto de ley que se somete a consideraci&#243;n de la Asamblea Nacional; ampliar el beneficio legal de alimentaci&#243;n que disfrutan los trabajadores a aquellas personas que dedicaron su vida productiva al trabajo y ahora se encuentran jubiladas o pensionadas. Este beneficio, que ha tenido gran &#233;xito y se ha convertido en uno de los beneficios m&#225;s apreciados por los trabajadores, es necesario que sea parte de los programas que ofrece el Estado a los adultos mayores, ofreci&#233;ndoles no solo un beneficio de alimentaci&#243;n sino tambi&#233;n la posibilidad de usar &#233;ste para la adquisici&#243;n de medicinas.
    </p>
    <p>
      
    </p>
    <p>
      La extensi&#243;n del beneficio de alimentaci&#243;n al jubilado o pensionado no es una petici&#243;n de unos pocos, sino un clamor popular. Es el reconocimiento a los ciudadanos que dedicaron su vida a ser &#243;rgano productivo de nuestro pa&#237;s. Es por ello, que no solo los interesados lo han solicitado ; sino todos los componentes de la sociedad han coincidido que se trata de un acto de reivindicaci&#243;n, de justicia social y una manifestaci&#243;n l&#243;gica y esperada del principio constitucional de progresividad que regula esta materia.
    </p>
    <p>
      
    </p>
    <p>
      El adulto mayor dedica fundamentalmente la pensi&#243;n o jubilaci&#243;n para su alimentaci&#243;n y salud. En el a&#241;o 2014 la inflaci&#243;n en Venezuela fue de 68,54% y para el mes de septiembre de 2015 la Canasta Alimentaria se encontraba en el orden de 38.873,12 bol&#237;vares seg&#250;n cifras INE y el monto de la pensi&#243;n era de 9.648,18 bol&#237;vares ( equivalente a un salario m&#237;nimo), por lo cual el deterioro del poder adquisitivo de su pensi&#243;n es dram&#225;tico. Esta circunstancia est&#225; obligando a nuestros pensionados y jubilados a decidir entre comer o cubrir un rubro absolutamente necesario como son los medicamentos, teniendo en cuenta que la mayor&#237;a de los Adultos Mayores son personas propensas a requerir de tratamientos m&#233;dicos permanentes que sobrepasan los ingresos de nuestros jubilados y pensionados.
    </p>
    <p>
      
    </p>
    <p>
      Por eso la Asamblea Nacional, garante de los derechos d el pueblo y en el ejercicio leg&#237;timo de su poder de legislar en materias de competencia nacional, siente la necesidad y la urgencia de crear la Ley de Bono de Alimentaci&#243;n y de Bono de Medicamentos para Jubilados y Pensionados del sector p&#250;blico y del sector privado.
    </p>
